#include <unordered_map>
#include <list>
#include <iostream>

struct MapNode{
    int value;
    std::list<int>::iterator node;
};

class LRUCache{
    int m_capacity;

    std::unordered_map<int, MapNode> keyMap;
    std::list<int> LRUQueue;

    inline void doVisit(MapNode& mn, int key)
    {
        LRUQueue.erase(mn.node);
        LRUQueue.push_front(key);
        mn.node = LRUQueue.begin();
    }

public:
    LRUCache(int capacity):m_capacity(capacity) {

    }

    int get(int key) {
        if (keyMap.count(key)>0)
        {
            MapNode& node = keyMap[key];
            doVisit(node, key);
            return node.value;
        }
        else
            return -1;
    }

    void set(int key, int value) {
        if (keyMap.count(key) > 0) {
            MapNode& node = keyMap[key];
            node.value = value;
            doVisit(node, key);
        }
        else {
            if (keyMap.size() >= m_capacity) {
                keyMap.erase(LRUQueue.back());
                LRUQueue.pop_back();
            }
            keyMap[key].value = value;
            LRUQueue.push_front(key);
            keyMap[key].node = LRUQueue.begin();
        }
    }
};
